@app.command()
def compare(
    config: Path = typer.Option(
        Path("canary.yaml"),
        "--config",
        "-c",
        help="Path to canary.yaml (used to run a fresh comparison).",
    ),
    provider: str | None = typer.Option(
        None,
        "--provider",
        "-p",
        help="Provider to run fresh comparison against (if --current not given).",
    ),
    baseline: Path | None = typer.Option(
        None,
        "--baseline",
        "-b",
        help="Path to baseline JSON file. If omitted, loads the most recent baseline.",
    ),
    current: Path | None = typer.Option(
        None,
        "--current",
        help="Path to current run JSON file. If omitted, runs a fresh query.",
    ),
    baseline_dir: Path = typer.Option(
        Path("baselines"),
        "--baseline-dir",
        help="Baseline storage directory.",
    ),
    output_md: Path | None = typer.Option(None, "--output-md"),
    output_html: Path | None = typer.Option(None, "--output-html"),
    fail_on_drift: bool = typer.Option(
        False,
        "--fail-on-drift",
        help="Exit with code 1 if drift is detected (for CI gating).",
    ),
    no_progress: bool = typer.Option(False, "--no-progress"),
) -> None:
    """Compare a current run against a saved baseline to detect drift.

    Either provide pre-existing JSON files via [cyan]--baseline[/cyan] and
    [cyan]--current[/cyan], or provide [cyan]--provider[/cyan] to run a
    fresh query and compare on the fly.

    [bold]Examples:[/bold]

    [dim]# Run fresh and compare to latest baseline[/dim]
    promptcanary compare --provider openai/gpt-5.4

    [dim]# Compare two saved files[/dim]
    promptcanary compare --baseline baselines/v1.json --current results.json --fail-on-drift
    """
    from promptcanary.core.comparator import compare as do_compare
    from promptcanary.core.models import CanaryRunResult
    from promptcanary.core.reporter import DriftReporter
    from promptcanary.storage.file import FileBaselineStore

    store = FileBaselineStore(baseline_dir)

    # Load baseline
    if baseline:
        console.print(f"[dim]Loading baseline from[/dim] [cyan]{baseline}[/cyan]…")
        baseline_snap = store.load_from_path(baseline)
    else:
        console.print(f"[dim]Loading latest baseline from[/dim] [cyan]{baseline_dir}/[/cyan]…")
        try:
            baseline_snap = store.load_latest(suite_name="*" if not config.exists() else "auto")
        except FileNotFoundError as e:
            err_console.print(
                f"[red]No baseline found:[/red] {e}\nRun with [cyan]--save-baseline[/cyan] first."
            )
            raise typer.Exit(1) from e

    # Load or generate current run
    if current:
        console.print(f"[dim]Loading current results from[/dim] [cyan]{current}[/cyan]…")
        data = json.loads(Path(current).read_text(encoding="utf-8"))
        current_result = CanaryRunResult.model_validate(data)
    elif provider:
        from promptcanary.core.suite import CanarySuite
        from promptcanary.providers.litellm import LiteLLMProvider

        if not config.exists():
            err_console.print(f"[red]Config not found:[/red] {config}")
            raise typer.Exit(1)

        suite = CanarySuite.from_yaml(config)
        llm = LiteLLMProvider(provider)
        console.print(f"[dim]Running fresh query against[/dim] [cyan]{provider}[/cyan]…")
        current_result = suite.run(llm, show_progress=not no_progress)
    else:
        err_console.print(
            "[red]Provide either --provider (for a fresh run) or --current (for a saved result).[/red]"
        )
        raise typer.Exit(1)

    # Compare
    try:
        drift = do_compare(baseline_snap, current_result)
    except ValueError as e:
        err_console.print(f"[red]Comparison error:[/red] {e}")
        raise typer.Exit(1) from e

    # Report
    reporter = DriftReporter(drift)
    reporter.print_terminal(console)

    if output_md:
        reporter.to_markdown(output_md)
        console.print(f"[dim]Markdown saved:[/dim] {output_md}")

    if output_html:
        reporter.to_html(output_html)
        console.print(f"[dim]HTML saved:[/dim] {output_html}")

    if fail_on_drift and drift.has_drift:
        raise typer.Exit(1)


