"""
promptcanary.cli
~~~~~~~~~~~~~~~~

PromptCanary CLI — built with Typer + Rich.

Commands:
  init     Scaffold a new canary suite directory.
  run      Run a canary suite against a provider.
  compare  Compare a saved baseline to a new run.
  report   Generate a report from saved JSON results.
  baselines List or inspect saved baselines.
"""

from __future__ import annotations

import json
from pathlib import Path

import typer
from rich.console import Console
from rich.panel import Panel

app = typer.Typer(
    name="promptcanary",
    help=(
        "🐦 PromptCanary — Detect silent behavioral drift in LLM providers.\n\n"
        "Catch when your provider silently changes — before it breaks production."
    ),
    add_completion=False,
    rich_markup_mode="rich",
    no_args_is_help=True,
)

console = Console()
err_console = Console(stderr=True)


# ─────────────────────────────────────────────────────────────────────────────
# init
# ─────────────────────────────────────────────────────────────────────────────


@app.command()
def init(
    name: str = typer.Argument(
        "my-canary-suite",
        help="Name of the directory and suite to create.",
    ),
    force: bool = typer.Option(False, "--force", "-f", help="Overwrite existing suite."),
) -> None:
    """Scaffold a new canary suite with example prompts and probes.

    Creates a ``canary.yaml``, a ``baselines/`` directory, and example files.
    """
    suite_dir = Path(name)
    if suite_dir.exists() and not force:
        err_console.print(
            f"[red]Directory '{name}' already exists.[/red] Use [cyan]--force[/cyan] to overwrite."
        )
        raise typer.Exit(1)

    suite_dir.mkdir(parents=True, exist_ok=True)
    (suite_dir / "baselines").mkdir(exist_ok=True)

    # Write example canary.yaml
    yaml_content = f"""\
# PromptCanary Suite — {name}
# Generated by `promptcanary init`
# Run: promptcanary run --config {name}/canary.yaml --provider openai/gpt-5.4

name: {name}
description: "Canary suite for detecting silent behavioral drift."
tags:
  - production
  - my-team

# ── Probes ────────────────────────────────────────────────────────────────────
# Each probe type checks a different aspect of model behavior.
# Mix and match to cover the dimensions that matter to your use case.
probes:
  - type: json_validity           # Does the model return valid JSON when asked?
  - type: keyword_presence        # Are expected keywords present?
    required_keywords: ["Paris"]
    forbidden_keywords: []
  - type: response_length         # Has verbosity drifted?
    min_chars: 10
    max_chars: 2000
  - type: refusal                 # Is the model refusing when it shouldn't?
    expect_refusal: false
  - type: direct_answer           # No "Great question!" preamble?
    expect_direct: true

# ── Prompts ────────────────────────────────────────────────────────────────────
# Add prompts that represent real patterns in your production system.
prompts:
  - text: "What is the capital of France? Reply in one sentence."
    description: "Basic geography — should be simple and direct."
    expected_keywords: ["Paris"]
    tags: [geography, simple]

  - text: "Return a JSON object with keys: name (string) and age (integer). Example values only."
    description: "JSON formatting canary — tests consistent output structure."
    tags: [json, format]

  - text: |
      Explain what photosynthesis is in 2-3 sentences.
      Keep it concise and use plain language.
    description: "Verbosity and clarity canary."
    tags: [explanation, verbosity]
"""
    (suite_dir / "canary.yaml").write_text(yaml_content, encoding="utf-8")

    # Write .env.example
    env_example = """\
# PromptCanary environment variables
# Copy to .env and fill in your API keys.

# OpenAI
OPENAI_API_KEY=sk-...

# Anthropic
ANTHROPIC_API_KEY=sk-ant-...

# Google
GEMINI_API_KEY=...
"""
    (suite_dir / ".env.example").write_text(env_example, encoding="utf-8")

    # Write README stub
    readme = f"""\
# {name} — PromptCanary Suite

This canary suite detects silent behavioral drift in your LLM provider.

## Quick Start

```bash
# Install dependencies
pip install promptcanary

# Set your API key
export OPENAI_API_KEY=sk-...

# Run and save baseline
promptcanary run --config canary.yaml --provider openai/gpt-5.4 --save-baseline

# Run again later to detect drift
promptcanary run --config canary.yaml --provider openai/gpt-5.4

# Compare against baseline
promptcanary compare --baseline baselines/latest.json --current results.json
```

## Adding to CI

See [PromptCanary docs](https://github.com/promptcanary/promptcanary) for GitHub Action setup.
"""
    (suite_dir / "README.md").write_text(readme, encoding="utf-8")

    console.print()
    console.print(
        Panel(
            f"[green bold]✅ Created canary suite: [cyan]{name}/[/cyan][/green bold]\n\n"
            f"  [dim]📄[/dim] [cyan]{name}/canary.yaml[/cyan]  — Edit your prompts and probes\n"
            f"  [dim]📁[/dim] [cyan]{name}/baselines/[/cyan]  — Baselines saved here\n"
            f"  [dim]📝[/dim] [cyan]{name}/.env.example[/cyan] — Copy to .env and add API keys\n\n"
            "[bold]Next steps:[/bold]\n"
            f"  1. [cyan]cd {name}[/cyan] and edit [cyan]canary.yaml[/cyan]\n"
            f"  2. Set your API key: [cyan]export OPENAI_API_KEY=sk-...[/cyan]\n"
            f"  3. Run: [cyan]promptcanary run --config canary.yaml --provider openai/gpt-5.4 --save-baseline[/cyan]",
            title="[bold blue]PromptCanary Init[/bold blue]",
            border_style="green",
        )
    )


