@app.command()
def baselines(
    baseline_dir: Path = typer.Option(Path("baselines"), "--dir", "-d"),
    suite: str | None = typer.Option(None, "--suite", "-s"),
) -> None:
    """List saved baseline snapshots."""
    from rich.table import Table

    from promptcanary.storage.file import FileBaselineStore

    store = FileBaselineStore(baseline_dir)
    items = store.list_baselines(suite_name=suite)

    if not items:
        console.print(f"[yellow]No baselines found in [cyan]{baseline_dir}[/cyan].[/yellow]")
        console.print("Run with [cyan]--save-baseline[/cyan] to create one.")
        return

    table = Table(title=f"Baselines — {baseline_dir}", show_lines=True)
    table.add_column("ID", style="cyan", no_wrap=True)
    table.add_column("Suite", style="bold")
    table.add_column("Model")
    table.add_column("Created At")
    table.add_column("Note")

    for item in items:
        snap_id = str(item["snapshot_id"])[:12]
        table.add_row(
            snap_id,
            str(item["suite_name"]),
            str(item["model_id"]),
            str(item["created_at"])[:19],
            str(item.get("note", "")),
        )

    console.print(table)


